//sqrt 
let sqrt = Math.sqrt(256);
console.log(`sqrt of 256 is ${sqrt}`);

//pow
let pow = Math.pow(2,3);
console.log(`result is ${pow}`)

//min
let numbers = Math.min(2,1,4,6,3,8,7,5,9);
console.log(`smallest number is : ${numbers}`)

//max
let max = Math.max(2,1,4,6,3,8,7,5,9);
console.log(`max number is : ${max}`)

//random,round
let random = Math.round(Math.random() * 10);
console.log(`random number is ${random}`);

//math.floor() => returns the largest integer less than or equal to a given number.
console.log(Math.floor(5.95));

console.log(Math.floor(5.05));

console.log(Math.floor(5));

console.log(Math.floor(-5.05));